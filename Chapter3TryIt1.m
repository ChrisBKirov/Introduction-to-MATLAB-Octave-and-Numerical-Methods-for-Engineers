% Function [A, B] = myTrigSum(a, b)

function [A, B] = ChapterTryIt1(a, b)
% Objective: Calculate simple trigonometric sums.   
% Input:
%   a,b - angles in radians.
% Output:
%   A = sin(a) + cos(b).
%   B = sin(b) + cos(a).
% Author: Chris B. Kirov
% Date: 25.08.2017
  
  A = sin(a) + cos(b);
  B = sin(b) + cos(a);
end
    