% Function [A] = myTriangle(b, h)

function [A] = Chapter3Exercise3(b,h)
% Objective: Calculate area of a triangle.
% Input:    
%   b - 1x1 double - base of triangle. 
%   h - 1x1 double - height of triangle.
% Output:    
%   A - area of a triangle.  
% Author: Chris B. Kirov
% Date: 26.08.2017
    
  A = (b * h) / 2.0;
    
end  